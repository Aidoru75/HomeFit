// Reverse mapping: numeric ID to exercise string ID
export const numericToExercise = Object.fromEntries(
  Object.entries(exerciseToNumeric).map(([key, value]) => [value, key])
);

// Get numeric ID for an exercise (returns null if not found)
export const getNumericId = (exerciseId) => {
  return exerciseToNumeric[exerciseId] || null;
};

// Get exercise string ID from numeric ID (returns null if not found)
export const getExerciseId = (numericId) => {
  return numericToExercise[numericId] || null;
};
